#include<iostream>
#include<cstdio>
#include<cstring>
#include<queue>
using namespace std;
long long d[32][5][1<<4][2];
int main()
{
	for(int n=2;n<=31;n++)
	{
		memset(d,0,sizeof d);
		for(int i=1;i<=n;i++)
		{
			if(i==1)
			{
				d[i][1][1][0]=1;
				d[i][2][2][0]=1;
				d[i][3][4][0]=1;
				d[i][4][8][0]=1;
			}
			else
			{
				for(int j=0;j<(1<<4);j++)
				{
					d[i][1][j|1][0]+=d[i-1][1][j][0]+d[i-1][2][j][0]+d[i-1][3][j][0];
					d[i][1][j|1][1]+=d[i-1][1][j][1]+d[i-1][2][j][1]+d[i-1][3][j][1]+d[i-1][4][j][1]+d[i-1][4][j][0];
					
					d[i][2][j|2][0]+=d[i-1][1][j][0]+d[i-1][2][j][0]+d[i-1][3][j][0]+d[i-1][4][j][0];
					d[i][2][j|2][1]+=d[i-1][1][j][1]+d[i-1][2][j][1]+d[i-1][3][j][1]+d[i-1][4][j][1];
					
					d[i][3][j|4][0]+=d[i-1][1][j][0]+d[i-1][2][j][0]+d[i-1][3][j][0]+d[i-1][4][j][0];
					d[i][3][j|4][1]+=d[i-1][1][j][1]+d[i-1][2][j][1]+d[i-1][3][j][1]+d[i-1][4][j][1];
					
					d[i][4][j|8][0]+=d[i-1][2][j][0]+d[i-1][3][j][0]+d[i-1][4][j][0];
					d[i][4][j|8][1]+=d[i-1][1][j][1]+d[i-1][2][j][1]+d[i-1][3][j][1]+d[i-1][4][j][1]+d[i-1][1][j][0];
				}
			}
		}
		long long ans=0;
		for(int i=1;i<=4;i++)ans+=d[n][i][15][1]+d[n][i][14][1]+d[n][i][13][1]+d[n][i][11][1]+d[n][i][7][1];
		printf("N=%d: %lld\n",n,ans);
	}
	return 0;
}